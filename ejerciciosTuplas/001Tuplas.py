### Tuplas ###
"""La principal diferencia entre una lista y una tupla es que las tuplas son inmutables, es decir, los datos no pueden
cambiar, por eso no se puede utilizar neither pop nor append nor insert porque debemos estar seguros que vamos a 
trabajr con esos datos durante todo el programa"""

my_tuple = tuple()
my_other_tuple = (1, 2)

print(my_other_tuple)


print(my_other_tuple[1])

print(my_other_tuple.count(2))

print(my_other_tuple.index(2))



